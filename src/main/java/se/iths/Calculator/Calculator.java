package se.iths.Calculator;

public class Calculator {

    //Method for addition with two numbers
    public int add(int a, int b){
        return a + b;
    }

    //Method for subtraction with two numbers 
    public int subract(int a, int b){
        return a - b;
    }

    //Method for multiplication with two numbers
    public int multiplication(int a, int b){
        return a * b;
    }

    //Method for division with two numbers
    public int division(int a, int b){
        return a / b;
    }
}
